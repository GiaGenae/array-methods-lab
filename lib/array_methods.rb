def using_include(array, element)
    array.include?(element)
end

def using_sort(array)
    array.sort!
end

def using_reverse(wow)
    wow.reverse
end

def using_first(wow)
    wow.first
end

def using_last(array)
    array.last
end

def using_size(array = "lemon," "lime," "apple," "plum," "peach," "banana")
    array.size
end
